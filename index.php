    // Fungsi untuk menghitung mundur
    function countdown() {
        // Tanggal target (ganti dengan tanggal dan waktu yang diinginkan)
        var targetDate = new Date("December 28, 2023 23:59:59").getTime();

        // Memperbarui hitungan mundur setiap 1 detik
        var x = setInterval(function () {
            // Tanggal dan waktu sekarang
            var now = new Date().getTime();

            // Hitung selisih antara tanggal target dan tanggal sekarang
            var distance = targetDate - now;

            // Hitung hari, jam, menit, dan detik
            var days = Math.floor(distance / (1000 * 60 * 60 * 24));
            var hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
            var minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
            var seconds = Math.floor((distance % (1000 * 60)) / 1000);

            // Menampilkan hasil hitungan mundur pada elemen HTML
            document.getElementById("days").innerHTML = days;
            document.getElementById("hours").innerHTML = hours;
            document.getElementById("minutes").innerHTML = minutes;
            document.getElementById("seconds").innerHTML = seconds;

            // Jika waktu target sudah tercapai, tampilkan pesan atau lakukan sesuatu yang diinginkan
            if (distance < 0) {
                clearInterval(x);
                document.getElementById("countdown").innerHTML = "Waktu sudah habis!";
            }
        }, 1000); // Update setiap 1 detik
    }

